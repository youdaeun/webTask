package day02;

public class Variable02 {
	public static void main(String[] args) {
		
	//자료형 변수명 = 값;
	int num1 = 2023; //4byte 정수의 기본형
	long num2 = 2023L; //8byte
	double num3 = 11.09; //8byte 실수의 기본형
	//정밀도에 의해서 double형이 기본 타입
	float num4 = 11.10f; //4byte
	
	char char1 = 'a';
	String name = "김영선";
	
	boolean isTrue = true;
	boolean isFalse = false;
	
	}
	
}
